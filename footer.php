<script>
function setCurrency(currency) {
    // Set the currency in a cookie
    document.cookie = "selectedCurrency=" + currency + ";path=/";

    // Update the display of prices and the dropdown button
    updatePriceDisplay(currency);
    updateDropdownButton(currency);
}

function updatePriceDisplay(currency) {
    var inrPrices = document.querySelectorAll('#inr');
    var usdPrices = document.querySelectorAll('#usd');

    if (currency === 'INR') {
        inrPrices.forEach(function(price) { price.style.display = 'block'; });
        usdPrices.forEach(function(price) { price.style.display = 'none'; });
    } else {
        inrPrices.forEach(function(price) { price.style.display = 'none'; });
        usdPrices.forEach(function(price) { price.style.display = 'block'; });
    }
}

function updateDropdownButton(currency) {
    var dropdownButton = document.querySelector('.currency-change');
    var flagSrc = currency === 'INR' ? 'img/india.png' : 'img/united-states.png';
    var flagAlt = currency === 'INR' ? 'India' : 'United States';

    dropdownButton.innerHTML = '<img src="' + flagSrc + '" alt="' + flagAlt + '"> ' + currency;
}

window.onload = function() {
    var selectedCurrency = getCookie('selectedCurrency') || 'INR';
    updatePriceDisplay(selectedCurrency);
    updateDropdownButton(selectedCurrency);
};

function getCookie(name) {
    var value = "; " + document.cookie;
    var parts = value.split("; " + name + "=");
    if (parts.length === 2) return parts.pop().split(";").shift();
}


